package com.milk.producer;

public class MilkProducer {

	private String name;
	private int milkProducerId;
	private String comments;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getMilkProducerId() {
		return milkProducerId;
	}

	public void setMilkProducerId(int milkProducerId) {
		this.milkProducerId = milkProducerId;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

}
